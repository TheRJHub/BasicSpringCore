package coom;

import java.util.Map;

public class Student {
String name;
Map<String, String> marks;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public Map<String, String> getMarks() {
	return marks;
}
public void setMarks(Map<String, String> marks) {
	this.marks = marks;
}

}
